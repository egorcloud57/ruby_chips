# Дан целочисленный массив. Найти индекс первого экстремального (то есть минимального или максимального) элемента.

# Встроенные методы всегда возвращают тот тип который они итерируют и указан в аргументе. Если мы используем встроенный метод и хотим вернуть другой тип который он не итерирует, например используя each который проходит по элементам и мы хотим вернуть индекс вместо элемента, можно воспользоваться оператором return и обернуть этот метод в лямду.

arr = [11, -121, 121, 1, 2, 18, 27, 15, 20]

func = ->(arr) do 
  arr.each_with_index do |el, i|
    return i if el == arr.max || el == arr.min
  end
end

p func.(arr)

