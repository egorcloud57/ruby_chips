# Дан целочисленный массив. Найти индекс последнего экстремального (то есть минимального или максимального) элемента.

arr3 = [11, -121, 121, -313, 1, 2, 18, 27, 15, 20]

func = ->(arr) do 
  min_ind = 0
  max_ind = 0
  arr.each_with_index do |el, i|
    if el == arr.max
      min_ind = i
    end
    if el == arr.min
      max_ind = i
    end
  end
  if min_ind > max_ind
    return min_ind
  else
    return max_ind
  end
end

p func.(arr3)